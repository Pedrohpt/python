# programa para receber dois números inteiros e realizar todas operações matematicas
# incluir resto da divisão e potencia

print("Programa para calcular operações matematicas")
num1 = float(input("Digite o primeiro número inteiro: "))
num2 = float(input("Digite o segundo número inteiro: "))

resultado = num1 + num2
print("A soma do primeiro e do segundo numero é: ", resultado)

resultado2 = num1 - num2
print("A subtração do primeiro e do segundo numero é: ", resultado2)

resultado3 = num1 * num2
print("A multiplicação do primeiro e do segundo numero é: ", resultado3)

resultado4 = num1 / num2
print("A divisão do primeiro e do segundo numero é: ", resultado4)

resultado5 = num1 % num2
print("O resto da divisão do primeiro e do segundo numero é: ", resultado5)

resultado6 = num1 ** num2
print("A potência do primeiro numero pelo segundo numero é: ", resultado6)
