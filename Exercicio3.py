print("Digete o primeiro numero")

num1 = input(int())

print("Digite o segundo numero")

num2 = input(int())

soma = int(num1) + int(num2)
subtracao = int(num1) - int(num2)
multiplicacao = int(num1) * int(num2)
divisao = int(num1) / int(num2)

print("A soma dos numeros é: " + str(soma))
print("A subtração dos numeros é: " + str(subtracao))
print("A multiplicação dos numeros é: " + str(multiplicacao))
print("A divisao dos numeros é: " + str(divisao))
